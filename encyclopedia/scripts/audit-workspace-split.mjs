import crypto from "node:crypto";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(scriptDir, "..");
const defaultWorkspaceRoot = path.resolve(appRoot, "..");
const defaultReferenceRoot = "F:\\Earthbound Decomp - Codex";

const args = process.argv.slice(2);
const options = {
  workspaceRoot: defaultWorkspaceRoot,
  referenceRoot: defaultReferenceRoot,
  json: false,
  limit: 12,
};

for (let i = 0; i < args.length; i += 1) {
  const arg = args[i];
  if (arg === "--workspace-root") {
    options.workspaceRoot = path.resolve(args[++i]);
  } else if (arg === "--reference-root") {
    options.referenceRoot = path.resolve(args[++i]);
  } else if (arg === "--json") {
    options.json = true;
  } else if (arg === "--limit") {
    options.limit = Number.parseInt(args[++i] ?? "", 10) || options.limit;
  } else {
    throw new Error(`Unknown argument: ${arg}`);
  }
}

const ITEM_POLICIES = new Map([
  [".gitattributes", ["repo-control", "Keep until the app repo layout is finalized."]],
  [".gitignore", ["repo-control", "Keep, then retune for the app-only release boundary."]],
  ["README.md", ["review-before-cleanup", "Root README may belong to the reference workspace or become app release notes. Review manually."]],
  ["encyclopedia", ["checked-in-app", "Keep in the app workspace."]],
  ["notes", ["reference-workspace", "Compare against the reference workspace before archiving from this workspace."]],
  ["tools", ["reference-workspace", "Compare against the reference workspace before archiving from this workspace."]],
  ["manifests", ["reference-workspace", "Compare against the reference workspace before archiving from this workspace."]],
  ["src", ["generated-local", "Regenerate locally from a ROM or reference workspace; do not distribute generated byte-equivalent source."]],
  ["asset-manifests", ["generated-local", "Regenerate or import locally; include only payload-free public manifests in release builds."]],
  ["build", ["generated-local", "Regenerate locally; archive only if needed for investigation."]],
  ["asm", ["rom-derived-local", "Archive or regenerate locally; never distribute extracted or byte-equivalent content."]],
  ["baserom", ["user-provided-local", "User-provided ROM material; never distribute."]],
  ["dumps", ["rom-derived-local", "Archive or regenerate locally; never distribute extracted content."]],
  ["refs", ["reference-local", "Reference checkouts are local-only; keep outside the app release package."]],
  ["inbox", ["scratch-local", "Scratch/automation input; review before archiving."]],
]);

const SKIP_DIRS = new Set([".git", "node_modules"]);
const CONTENT_COMPARE_CATEGORIES = new Set([
  "reference-workspace",
  "generated-local",
  "rom-derived-local",
  "user-provided-local",
  "reference-local",
  "scratch-local",
  "review-before-cleanup",
]);

function policyFor(name) {
  if (/^tmp.*\.asm$/i.test(name)) {
    return ["scratch-local", "Temporary ASM probe; archive only if still useful."];
  }
  if (/\.(sfc|smc|fig|rom)$/i.test(name)) {
    return ["user-provided-local", "User-provided ROM; never distribute."];
  }
  return ITEM_POLICIES.get(name) ?? ["unknown", "Review before cleanup."];
}

async function exists(targetPath) {
  try {
    await fs.access(targetPath);
    return true;
  } catch {
    return false;
  }
}

function formatBytes(bytes) {
  if (bytes < 1024) return `${bytes} B`;
  const units = ["KB", "MB", "GB"];
  let size = bytes / 1024;
  let unit = units.shift();
  while (size >= 1024 && units.length) {
    size /= 1024;
    unit = units.shift();
  }
  return `${size.toFixed(size >= 10 ? 1 : 2)} ${unit}`;
}

function isoTime(value) {
  return value ? new Date(value).toISOString() : null;
}

async function hashFile(filePath) {
  const data = await fs.readFile(filePath);
  return crypto.createHash("sha1").update(data).digest("hex");
}

async function scanPath(targetPath, rootPath = targetPath) {
  const stats = await fs.stat(targetPath);
  if (stats.isFile()) {
    const rel = path.relative(rootPath, targetPath).replaceAll(path.sep, "/") || path.basename(targetPath);
    return {
      files: 1,
      dirs: 0,
      bytes: stats.size,
      newestMtimeMs: stats.mtimeMs,
      entries: new Map([[rel, { bytes: stats.size, sha1: await hashFile(targetPath), mtimeMs: stats.mtimeMs }]]),
    };
  }

  const result = {
    files: 0,
    dirs: 1,
    bytes: 0,
    newestMtimeMs: stats.mtimeMs,
    entries: new Map(),
  };

  const children = await fs.readdir(targetPath, { withFileTypes: true });
  for (const child of children) {
    if (child.isDirectory() && SKIP_DIRS.has(child.name)) continue;
    const childPath = path.join(targetPath, child.name);
    const childScan = await scanPath(childPath, rootPath);
    result.files += childScan.files;
    result.dirs += childScan.dirs;
    result.bytes += childScan.bytes;
    result.newestMtimeMs = Math.max(result.newestMtimeMs, childScan.newestMtimeMs);
    for (const [rel, entry] of childScan.entries) {
      result.entries.set(rel, entry);
    }
  }

  return result;
}

function fingerprint(scan) {
  const hash = crypto.createHash("sha1");
  for (const [rel, entry] of [...scan.entries].sort(([a], [b]) => a.localeCompare(b))) {
    hash.update(rel);
    hash.update("\0");
    hash.update(String(entry.bytes));
    hash.update("\0");
    hash.update(entry.sha1);
    hash.update("\0");
  }
  return hash.digest("hex");
}

function compareScans(workspaceScan, referenceScan, limit) {
  if (!referenceScan) {
    return {
      status: "missing-in-reference",
      missingInReference: [],
      changedFromReference: [],
      extraInReference: [],
    };
  }

  const missingInReference = [];
  const changedFromReference = [];
  const extraInReference = [];

  for (const [rel, workspaceEntry] of workspaceScan.entries) {
    const referenceEntry = referenceScan.entries.get(rel);
    if (!referenceEntry) {
      if (missingInReference.length < limit) missingInReference.push(rel);
    } else if (workspaceEntry.sha1 !== referenceEntry.sha1) {
      if (changedFromReference.length < limit) changedFromReference.push(rel);
    }
  }

  for (const rel of referenceScan.entries.keys()) {
    if (!workspaceScan.entries.has(rel) && extraInReference.length < limit) {
      extraInReference.push(rel);
    }
  }

  if (fingerprint(workspaceScan) === fingerprint(referenceScan)) {
    return { status: "matches-reference", missingInReference, changedFromReference, extraInReference };
  }
  if (missingInReference.length || changedFromReference.length) {
    return { status: "differs-from-reference", missingInReference, changedFromReference, extraInReference };
  }
  return { status: "reference-has-extra-files", missingInReference, changedFromReference, extraInReference };
}

async function audit() {
  const workspaceRoot = path.resolve(options.workspaceRoot);
  const referenceRoot = path.resolve(options.referenceRoot);
  const referenceAvailable = await exists(referenceRoot);
  const children = await fs.readdir(workspaceRoot, { withFileTypes: true });
  const rows = [];

  for (const child of children.sort((a, b) => a.name.localeCompare(b.name))) {
    if (child.name === ".git") continue;
    const [category, recommendation] = policyFor(child.name);
    const workspacePath = path.join(workspaceRoot, child.name);
    const workspaceScan = await scanPath(workspacePath);
    const row = {
      name: child.name,
      kind: child.isDirectory() ? "dir" : "file",
      category,
      recommendation,
      files: workspaceScan.files,
      dirs: workspaceScan.dirs,
      bytes: workspaceScan.bytes,
      newest: isoTime(workspaceScan.newestMtimeMs),
      referenceStatus: "not-compared",
      missingInReference: [],
      changedFromReference: [],
      extraInReference: [],
    };

    if (referenceAvailable && CONTENT_COMPARE_CATEGORIES.has(category)) {
      const referencePath = path.join(referenceRoot, child.name);
      if (await exists(referencePath)) {
        const referenceScan = await scanPath(referencePath);
        const comparison = compareScans(workspaceScan, referenceScan, options.limit);
        row.referenceStatus = comparison.status;
        row.missingInReference = comparison.missingInReference;
        row.changedFromReference = comparison.changedFromReference;
        row.extraInReference = comparison.extraInReference;
      } else {
        row.referenceStatus = "missing-in-reference";
      }
    } else if (!referenceAvailable) {
      row.referenceStatus = "reference-root-not-found";
    }

    rows.push(row);
  }

  return {
    checkedAt: new Date().toISOString(),
    workspaceRoot,
    referenceRoot,
    referenceAvailable,
    rows,
  };
}

function renderMarkdown(report) {
  const lines = [];
  lines.push("# Workspace Split Audit");
  lines.push("");
  lines.push(`- Workspace: \`${report.workspaceRoot}\``);
  lines.push(`- Reference: \`${report.referenceRoot}\`${report.referenceAvailable ? "" : " (not found)"}`);
  lines.push(`- Checked: ${report.checkedAt}`);
  lines.push("");
  lines.push("This audit is read-only. It classifies top-level workspace material before any archive, move, or cleanup step.");
  lines.push("");
  lines.push("| Item | Policy | Files | Size | Reference status | Recommendation |");
  lines.push("| --- | --- | ---: | ---: | --- | --- |");
  for (const row of report.rows) {
    lines.push(
      `| \`${row.name}\` | ${row.category} | ${row.files} | ${formatBytes(row.bytes)} | ${row.referenceStatus} | ${row.recommendation} |`,
    );
  }

  const riskyRows = report.rows.filter(
    (row) => row.referenceStatus === "missing-in-reference" || row.referenceStatus === "differs-from-reference",
  );
  if (riskyRows.length) {
    lines.push("");
    lines.push("## Differences To Review Before Cleanup");
    for (const row of riskyRows) {
      lines.push("");
      lines.push(`### ${row.name}`);
      lines.push(`- Status: ${row.referenceStatus}`);
      if (row.missingInReference.length) {
        lines.push(`- Present here but missing in reference: ${row.missingInReference.map((rel) => `\`${rel}\``).join(", ")}`);
      }
      if (row.changedFromReference.length) {
        lines.push(`- Different from reference: ${row.changedFromReference.map((rel) => `\`${rel}\``).join(", ")}`);
      }
      if (row.extraInReference.length) {
        lines.push(`- Reference-only examples: ${row.extraInReference.map((rel) => `\`${rel}\``).join(", ")}`);
      }
    }
  }

  lines.push("");
  lines.push("Suggested next cleanup gate: archive or reconcile every item with `missing-in-reference` or `differs-from-reference` before removing decomp material from the app workspace.");
  return lines.join("\n");
}

const report = await audit();
if (options.json) {
  console.log(JSON.stringify(report, null, 2));
} else {
  console.log(renderMarkdown(report));
}
