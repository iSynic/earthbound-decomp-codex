from __future__ import annotations

import argparse
import json
from pathlib import Path

from add_source_bank_range import ROOT, build_entry, recalculate_summary
from rom_tools import find_rom, load_rom


def parse_bank_address(raw: str) -> tuple[str, int]:
    bank, address = raw.split(":", 1)
    return bank.upper(), int(address, 16)


def default_terminal_label(end: str, name: str) -> str:
    bank, address = parse_bank_address(end)
    if address > 0xFFFF:
        return ""
    return f"{bank}{address:04X}_{name}End"


def write_data_stub(path: Path, *, bank: str, start: str, end: str, name: str, title: str, terminal_label: str, force: bool) -> None:
    if path.exists() and not force:
        raise SystemExit(f"{path} already exists; pass --force to replace it")
    path.parent.mkdir(parents=True, exist_ok=True)
    terminal_block = (
        f"""; ---------------------------------------------------------------------------
; {end}

; {name}End
{terminal_label}:
"""
        if terminal_label
        else f"""; ---------------------------------------------------------------------------
; {end}

; {name} has no terminal label because the range ends at the next bank boundary.
"""
    )
    text = f"""; EarthBound {bank} {title}.
;
; Source-emission status:
; - Prototype level: build-candidate data corridor.
; - Generated by tools/promote_source_bank_data_range.py.
; - ROM bytes are preserved by build/{bank.lower()}-build-candidate-ranges.json
;   as one data gap before the terminal label below.
;
; Source units covered:
; - {start}..{end} {name}

{terminal_block}"""
    path.write_text(text, encoding="utf-8", newline="\n")


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Create a data-corridor source stub and register it in a source-bank manifest."
    )
    parser.add_argument("--bank", required=True)
    parser.add_argument("--source-path", type=Path, required=True)
    parser.add_argument("--subsystem", required=True)
    parser.add_argument("--start", required=True)
    parser.add_argument("--end", required=True)
    parser.add_argument("--name", required=True)
    parser.add_argument("--title", required=True)
    parser.add_argument("--terminal-label")
    parser.add_argument("--evidence", action="append", default=[])
    parser.add_argument("--rom")
    parser.add_argument("--manifest", type=Path)
    parser.add_argument("--force", action="store_true")
    return parser


def main() -> int:
    args = build_parser().parse_args()
    bank = args.bank.upper()
    start_bank, _ = parse_bank_address(args.start)
    end_bank, _ = parse_bank_address(args.end)
    if start_bank != bank or end_bank != bank:
        raise SystemExit(f"range must stay within bank {bank}: {args.start}..{args.end}")

    source_path = args.source_path
    if not source_path.is_absolute():
        source_path = ROOT / source_path
    terminal_label = args.terminal_label or default_terminal_label(args.end, args.name)
    write_data_stub(
        source_path,
        bank=bank,
        start=args.start,
        end=args.end,
        name=args.name,
        title=args.title,
        terminal_label=terminal_label,
        force=args.force,
    )

    manifest_path = args.manifest or ROOT / "build" / f"{bank.lower()}-build-candidate-ranges.json"
    if not manifest_path.is_absolute():
        manifest_path = ROOT / manifest_path

    rom = load_rom(find_rom(args.rom))
    entry_args = argparse.Namespace(
        bank=bank,
        source_path=Path(args.source_path.as_posix()),
        subsystem=args.subsystem,
        start=args.start,
        end=args.end,
        name=args.name,
        source_segment=[],
        data_gap=[f"{args.start},{args.end},{args.name}"],
        evidence=args.evidence,
        rom=args.rom,
        manifest=manifest_path,
    )
    manifest = json.loads(manifest_path.read_text(encoding="utf-8"))
    entry = build_entry(entry_args, rom)
    ranges = [
        item
        for item in manifest.get("ranges", [])
        if item.get("source_path") != entry["source_path"]
    ]
    ranges.append(entry)
    manifest["ranges"] = ranges
    recalculate_summary(manifest)
    manifest_path.write_text(
        json.dumps(manifest, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
        newline="\n",
    )
    print(
        f"wrote {source_path.relative_to(ROOT)} and {manifest_path.relative_to(ROOT)}: "
        f"{args.start}..{args.end} ({entry['size']} bytes)"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
