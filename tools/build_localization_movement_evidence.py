#!/usr/bin/env python3
"""Summarize localization-derived movement evidence without recovered text.

This consumes the ignored localization/map-object crosswalk and the tracked
map movement usage contract. The output note contains only movement IDs,
ROM-backed addresses, counts, and existing public labels; recovered
ActionScript descriptor values stay in the ignored crosswalk JSON.
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_CROSSWALK = ROOT / "build" / "localization-map-object-crosswalk.json"
DEFAULT_MOVEMENT_USAGE = ROOT / "notes" / "map-movement-usage-contract.json"
DEFAULT_NOTE = ROOT / "notes" / "localization-movement-evidence.md"


def load_json(path: Path) -> Any:
    return json.loads(path.read_text(encoding="utf-8"))


def first_values(items: list[dict[str, Any]], limit: int = 3) -> str:
    values = []
    for item in items[:limit]:
        value = item.get("value", "")
        count = item.get("count", 0)
        values.append(f"{value} ({count})")
    return ", ".join(values)


def build_rows(crosswalk: dict[str, Any], movement_usage: dict[str, Any]) -> list[dict[str, Any]]:
    usage_by_id = {int(row["movement_id"]): row for row in movement_usage["movement_usage"]}
    object_ids_by_movement: dict[int, set[str]] = {}
    record_links_by_movement: dict[int, int] = {}
    for object_row in crosswalk["map_object_rows"]:
        movement_id = int(object_row["movement_id"])
        linked_records = {
            key
            for slot in object_row["text_slots"]
            for key in slot["localization_record_keys"]
        }
        if not linked_records:
            continue
        object_ids_by_movement.setdefault(movement_id, set()).add(object_row["object_id"])
        record_links_by_movement[movement_id] = record_links_by_movement.get(movement_id, 0) + len(linked_records)

    rows = []

    for row in crosswalk["movement_descriptor_summary"]:
        movement_id = int(row["movement_id"])
        usage = usage_by_id.get(movement_id, {})
        rows.append(
            {
                "movement_id": movement_id,
                "target_label": usage.get("target_label", ""),
                "pointer_target_address": usage.get("pointer_target_address", ""),
                "behavior_bucket": usage.get("behavior_bucket", ""),
                "map_placement_count": usage.get("map_placement_count", 0),
                "matched_objects": len(object_ids_by_movement.get(movement_id, set())),
                "matched_record_links": record_links_by_movement.get(movement_id, 0),
                "descriptor_classes": len(row["descriptors"]),
                "top_sprites": first_values(usage.get("top_sprites", [])),
            }
        )

    rows.sort(key=lambda item: (-item["matched_record_links"], -item["descriptor_classes"], item["movement_id"]))
    return rows


def write_note(rows: list[dict[str, Any]], crosswalk: dict[str, Any], note_path: Path) -> None:
    summary = crosswalk["summary"]
    lines = [
        "# Localization-derived movement evidence",
        "",
        "Generated by `tools/build_localization_movement_evidence.py`.",
        "",
        "This report distills the ignored recovered-source crosswalk into public-safe",
        "movement evidence. It records movement IDs, ROM-backed script targets, counts,",
        "and existing public labels only. Recovered `;@ActionScript:` descriptor text",
        "stays in `build/localization-map-object-crosswalk.json`.",
        "",
        "## Summary",
        "",
        f"- movement IDs with localization-derived descriptor evidence: `{len(rows)}`",
        f"- matched map objects: `{summary['map_objects_matched_to_localization_record']}`",
        f"- matched recovered records: `{summary['records_matched_to_map_object_text_pointer']}`",
        f"- matched descriptor classes: `{summary['unique_actionscript_descriptors_matched']}`",
        "",
        "## Highest-Leverage Movement IDs",
        "",
        "| Movement | Target | Address | Bucket | Map placements | Matched objects | Record links | Descriptor classes | Top sprites |",
        "| ---: | --- | --- | --- | ---: | ---: | ---: | ---: | --- |",
    ]
    for row in rows[:32]:
        lines.append(
            "| {movement_id} | `{target_label}` | `{pointer_target_address}` | `{behavior_bucket}` | "
            "{map_placement_count} | {matched_objects} | {matched_record_links} | "
            "{descriptor_classes} | {top_sprites} |".format(**row)
        )

    lines.extend(
        [
            "",
            "## Use",
            "",
            "- Use the high-count rows to prioritize C3 source-pilot promotion and naming",
            "  of common map-object behavior scripts.",
            "- Use the ignored crosswalk JSON locally when the exact recovered",
            "  `;@ActionScript:` descriptor value is needed for a working name.",
            "- Do not assume a movement ID has one semantic role just because one",
            "  descriptor dominates; common movement IDs are intentionally reused.",
            "",
        ]
    )
    note_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--crosswalk", type=Path, default=DEFAULT_CROSSWALK)
    parser.add_argument("--movement-usage", type=Path, default=DEFAULT_MOVEMENT_USAGE)
    parser.add_argument("--note", type=Path, default=DEFAULT_NOTE)
    args = parser.parse_args()

    crosswalk = load_json(args.crosswalk)
    movement_usage = load_json(args.movement_usage)
    rows = build_rows(crosswalk, movement_usage)
    write_note(rows, crosswalk, args.note)

    print(f"wrote {args.note}")
    print(f"movement_ids={len(rows)} matched_objects={crosswalk['summary']['map_objects_matched_to_localization_record']}")


if __name__ == "__main__":
    main()
