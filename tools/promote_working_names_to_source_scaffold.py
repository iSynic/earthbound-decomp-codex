from __future__ import annotations

import argparse
import hashlib
import json
import re
from pathlib import Path
from typing import Any

from rom_tools import find_rom, hirom_to_file_offset, load_rom


ROOT = Path(__file__).resolve().parent.parent
SCHEMA = "earthbound-decomp.source-bank-ranges.v1"


def parse_cpu_address(raw: str) -> tuple[str, int]:
    bank, address = raw.split(":", 1)
    return bank.upper(), int(address, 16)


def format_cpu_address(bank: str, address: int) -> str:
    return f"{bank.upper()}:{address:04X}" if address <= 0xFFFF else f"{bank.upper()}:10000"


def file_offset_for(rom: bytes, bank: str, address: int) -> str:
    offset = hirom_to_file_offset(int(bank, 16), address, len(rom))
    if offset is None:
        raise SystemExit(f"could not convert {format_cpu_address(bank, address)}")
    return f"0x{offset:06X}"


def rom_bytes_for(rom: bytes, bank: str, start: int, end: int) -> bytes:
    start_offset = hirom_to_file_offset(int(bank, 16), start, len(rom))
    end_offset = hirom_to_file_offset(int(bank, 16), end, len(rom))
    if start_offset is None or end_offset is None:
        raise SystemExit(
            f"could not convert {format_cpu_address(bank, start)}..{format_cpu_address(bank, end)}"
        )
    return rom[start_offset:end_offset]


def byte_preview(data: bytes, *, tail: bool = False) -> str:
    chunk = data[-16:] if tail else data[:16]
    return " ".join(f"{byte:02X}" for byte in chunk)


def slug(raw: str) -> str:
    text = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", raw)
    text = re.sub(r"[^A-Za-z0-9]+", "_", text).strip("_").lower()
    return text or "unnamed"


def label_name(bank: str, address: int, name: str) -> str:
    return f"{bank.upper()}{address:04X}_{re.sub(r'[^A-Za-z0-9_]', '_', name)}"


def evidence_strings(entry: dict[str, Any]) -> list[str]:
    evidence: list[str] = []
    for item in entry.get("evidence", []):
        if isinstance(item, str):
            evidence.append(item)
            continue
        note = item.get("note", "unknown-note")
        line = item.get("line")
        section = item.get("section")
        rendered = note
        if line is not None:
            rendered += f":{line}"
        if section:
            rendered += f" ({section})"
        evidence.append(rendered)
    confidence = entry.get("confidence")
    status = entry.get("status")
    tags = entry.get("tags", [])
    if confidence or status or tags:
        tag_text = ", ".join(tags) if tags else "untagged"
        evidence.append(
            f"working-name metadata: status={status or 'unknown'}, confidence={confidence or 'unknown'}, tags={tag_text}"
        )
    return evidence


def collect_bank_entries(working_names: dict[str, Any], bank: str) -> dict[int, dict[str, Any]]:
    by_address: dict[int, dict[str, Any]] = {}
    for entry in working_names.get("entries", []):
        entry_bank, address = parse_cpu_address(entry["address"])
        if entry_bank != bank:
            continue
        current = by_address.get(address)
        if current is None:
            by_address[address] = {
                "address": address,
                "name": entry["name"],
                "aliases": [],
                "evidence": evidence_strings(entry),
            }
            continue
        current["aliases"].append(entry["name"])
        current["evidence"].extend(evidence_strings(entry))
    return by_address


def write_stub(
    path: Path,
    *,
    bank: str,
    start: int,
    end: int,
    name: str,
    aliases: list[str],
    force: bool,
) -> None:
    if path.exists() and not force:
        raise SystemExit(f"{path} already exists; pass --force to replace it")
    path.parent.mkdir(parents=True, exist_ok=True)
    start_label = label_name(bank, start, name)
    terminal = ""
    if end <= 0xFFFF:
        terminal = (
            f"\n; ---------------------------------------------------------------------------\n"
            f"; {format_cpu_address(bank, end)}\n\n"
            f"{label_name(bank, end, name + '_End')}:\n"
        )
    else:
        terminal = (
            "\n; ---------------------------------------------------------------------------\n"
            f"; {format_cpu_address(bank, end)}\n\n"
            "; This range ends at the next bank boundary, so it has no terminal label.\n"
        )

    alias_lines = "\n".join(f"; - {alias}" for alias in aliases) if aliases else "; - none"
    text = f"""; EarthBound {bank.upper()} working-name anchored byte corridor.
;
; Source-emission status:
; - Prototype level: build-candidate byte corridor.
; - Generated by tools/promote_working_names_to_source_scaffold.py.
; - The current scaffold preserves ROM bytes exactly as data before the
;   terminal label. Replace this corridor with decoded source when the
;   routine/data contract is promoted.
;
; Source unit covered:
; - {format_cpu_address(bank, start)}..{format_cpu_address(bank, end)} {name}
;
; Aliases at the same address:
{alias_lines}

; ---------------------------------------------------------------------------
; {format_cpu_address(bank, start)}

{start_label}:
{terminal}"""
    path.write_text(text, encoding="utf-8", newline="\n")


def build_range(
    *,
    bank: str,
    start: int,
    end: int,
    name: str,
    aliases: list[str],
    evidence: list[str],
    source_path: str,
    rom: bytes,
) -> dict[str, Any]:
    data = rom_bytes_for(rom, bank, start, end)
    sha1 = hashlib.sha1(data).hexdigest()
    start_address = format_cpu_address(bank, start)
    end_address = format_cpu_address(bank, end)
    gap = {
        "kind": "data-gap",
        "start": start_address,
        "end": end_address,
        "size": len(data),
        "file_offset_start": file_offset_for(rom, bank, start),
        "file_offset_end": file_offset_for(rom, bank, end),
        "sha1": sha1,
        "first_bytes": byte_preview(data),
        "last_bytes": byte_preview(data, tail=True),
        "name": name,
        "labels": [f"{start_address} {name}"],
        "evidence": evidence,
    }
    labels = [f"{start_address} {name}"]
    labels.extend(f"{start_address} alias {alias}" for alias in aliases)
    return {
        "source_path": source_path,
        "subsystem": "working-name-byte-corridor",
        "level": "build-candidate",
        "start": start_address,
        "end": end_address,
        "size": len(data),
        "file_offset_start": file_offset_for(rom, bank, start),
        "file_offset_end": file_offset_for(rom, bank, end),
        "sha1": sha1,
        "first_bytes": byte_preview(data),
        "last_bytes": byte_preview(data, tail=True),
        "labels": labels,
        "evidence": evidence,
        "source_size": 0,
        "data_gap_size": len(data),
        "source_segments": [],
        "data_gaps": [gap],
    }


def build_manifest(bank: str, ranges: list[dict[str, Any]]) -> dict[str, Any]:
    total = sum(int(item["size"]) for item in ranges)
    data_gap = sum(int(item["data_gap_size"]) for item in ranges)
    return {
        "schema": SCHEMA,
        "generated_by": "tools/promote_working_names_to_source_scaffold.py",
        "bank": bank.upper(),
        "summary": {
            "ranges": len(ranges),
            "total_bytes": total,
            "source_bytes": 0,
            "data_gap_bytes": data_gap,
            "anchor_model": "working-name intervals",
        },
        "ranges": ranges,
    }


def default_working_names(bank: str) -> Path:
    bank_path = ROOT / "build" / f"working-names-{bank.lower()}.json"
    if bank_path.exists():
        return bank_path
    return ROOT / "build" / "working-names-c0-c2.json"


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Promote working-name anchors into a byte-equivalent source scaffold manifest."
    )
    parser.add_argument("--bank", required=True)
    parser.add_argument("--working-names", type=Path)
    parser.add_argument("--manifest-out", type=Path)
    parser.add_argument("--src-dir", type=Path)
    parser.add_argument("--rom")
    parser.add_argument("--force", action="store_true")
    args = parser.parse_args()

    bank = args.bank.upper()
    working_names_path = args.working_names or default_working_names(bank)
    if not working_names_path.is_absolute():
        working_names_path = ROOT / working_names_path
    working_names = json.loads(working_names_path.read_text(encoding="utf-8"))
    entries = collect_bank_entries(working_names, bank)
    if not entries:
        raise SystemExit(f"no working-name entries found for bank {bank} in {working_names_path}")

    rom = load_rom(find_rom(args.rom))
    src_dir = args.src_dir or ROOT / "src" / bank.lower()
    if not src_dir.is_absolute():
        src_dir = ROOT / src_dir
    manifest_out = args.manifest_out or ROOT / "build" / f"{bank.lower()}-build-candidate-ranges.json"
    if not manifest_out.is_absolute():
        manifest_out = ROOT / manifest_out

    anchors = sorted(entries)
    if anchors[0] != 0:
        entries[0] = {
            "address": 0,
            "name": "BankPrefixByteCorridor",
            "aliases": [],
            "evidence": [f"synthetic prefix before first working-name anchor {format_cpu_address(bank, anchors[0])}"],
        }
        anchors = [0, *anchors]

    ranges: list[dict[str, Any]] = []
    for index, start in enumerate(anchors):
        end = anchors[index + 1] if index + 1 < len(anchors) else 0x10000
        if end <= start:
            continue
        entry = entries[start]
        name = entry["name"]
        path = src_dir / f"{bank.lower()}_{start:04x}_{slug(name)}.asm"
        source_path = path.relative_to(ROOT).as_posix()
        write_stub(
            path,
            bank=bank,
            start=start,
            end=end,
            name=name,
            aliases=entry.get("aliases", []),
            force=args.force,
        )
        ranges.append(
            build_range(
                bank=bank,
                start=start,
                end=end,
                name=name,
                aliases=entry.get("aliases", []),
                evidence=entry.get("evidence", []),
                source_path=source_path,
                rom=rom,
            )
        )

    manifest = build_manifest(bank, ranges)
    manifest_out.parent.mkdir(parents=True, exist_ok=True)
    manifest_out.write_text(
        json.dumps(manifest, indent=2, ensure_ascii=False) + "\n",
        encoding="utf-8",
        newline="\n",
    )
    print(
        f"promoted {len(ranges)} {bank} working-name corridor(s) from {working_names_path.relative_to(ROOT)}"
    )
    print(f"wrote {manifest_out.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
