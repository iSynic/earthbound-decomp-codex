#!/usr/bin/env python3
"""Catalog local EarthBound SRM zip anchors for Mesen scenario planning."""

from __future__ import annotations

import argparse
import hashlib
import json
import re
import shutil
import zipfile
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parent.parent
DEFAULT_SAVE_ROOT = Path(r"F:\Earthbound Saves")
DEFAULT_EXTRACT_ROOT = ROOT / "build" / "mesen-scenarios" / "srm-anchors"
DEFAULT_OUTPUT = ROOT / "manifests" / "mesen-srm-anchor-catalog.json"
DEFAULT_NOTE = ROOT / "notes" / "mesen-srm-anchor-catalog.md"
SRM_SIZE = 8192


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--save-root", default=str(DEFAULT_SAVE_ROOT))
    parser.add_argument("--extract-root", default=str(DEFAULT_EXTRACT_ROOT))
    parser.add_argument("--output", default=str(DEFAULT_OUTPUT))
    parser.add_argument("--note", default=str(DEFAULT_NOTE))
    parser.add_argument("--no-extract", action="store_true", help="Catalog only; do not write ignored working SRMs.")
    return parser.parse_args()


def sha256_bytes(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


def write_json(path: Path, data: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(data, indent=2) + "\n", encoding="utf-8")


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def manifest_path(path: Path) -> str:
    try:
        return path.resolve().relative_to(ROOT).as_posix()
    except ValueError:
        return str(path)


def anchor_label(stem: str) -> str:
    match = re.match(r"earthbsave(\d+)-(.+)$", stem, re.IGNORECASE)
    if not match:
        return stem
    return f"{int(match.group(1)):02d}-{match.group(2)}"


def read_zip_anchor(zip_path: Path, extract_root: Path, *, extract: bool) -> dict[str, Any]:
    with zipfile.ZipFile(zip_path) as archive:
        srm_entries = [entry for entry in archive.infolist() if entry.filename.lower().endswith(".srm")]
        readme_entries = [entry for entry in archive.infolist() if entry.filename.lower().endswith("readme.txt")]
        if len(srm_entries) != 1:
            raise ValueError(f"{zip_path}: expected exactly one SRM, found {len(srm_entries)}")
        srm_entry = srm_entries[0]
        srm_data = archive.read(srm_entry)
        readme_text = ""
        if readme_entries:
            readme_text = archive.read(readme_entries[0]).decode("utf-8", errors="replace").strip()
    label = anchor_label(zip_path.stem)
    working_path = extract_root / label / "earthbound.srm"
    if extract:
        working_path.parent.mkdir(parents=True, exist_ok=True)
        working_path.write_bytes(srm_data)
    return {
        "anchor_id": label,
        "archive_name": zip_path.name,
        "archive_sha256": hashlib.sha256(zip_path.read_bytes()).hexdigest(),
        "readme_text": readme_text,
        "srm_entry": srm_entry.filename,
        "srm_size": len(srm_data),
        "srm_sha256": sha256_bytes(srm_data),
        "working_srm_path": manifest_path(working_path) if extract else None,
        "evidence_role": "vanilla_srm_anchor",
        "source_promotion_allowed": False,
    }


def render_note(data: dict[str, Any]) -> str:
    lines = [
        "# Mesen SRM Anchor Catalog",
        "",
        "Generated by `tools/build_mesen_srm_anchor_catalog.py` from the local",
        "SRM zip archive folder. The SRM bytes remain local and ignored; this note",
        "records only metadata and hashes for scenario planning.",
        "",
        "## Summary",
        "",
        f"- save root: `{data['save_root']}`",
        f"- archives: `{data['summary']['archive_count']}`",
        f"- valid 8192-byte SRMs: `{data['summary']['valid_srm_count']}`",
        f"- extracted working copies: `{data['summary']['extracted_working_copy_count']}`",
        "- source promotion allowed: `False`",
        "",
        "## Anchors",
        "",
        "| Anchor | Archive | SRM bytes | Readme summary |",
        "| --- | --- | ---: | --- |",
    ]
    for row in data["anchors"]:
        readme = " ".join(str(row["readme_text"]).split())
        if len(readme) > 110:
            readme = readme[:107] + "..."
        lines.append(
            f"| `{row['anchor_id']}` | `{row['archive_name']}` | `{row['srm_size']}` | {readme.replace('|', '\\|')} |"
        )
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    args = parse_args()
    save_root = Path(args.save_root)
    extract_root = Path(args.extract_root)
    if not save_root.is_dir():
        raise FileNotFoundError(f"SRM save root not found: {save_root}")
    if extract_root.exists() and not args.no_extract:
        shutil.rmtree(extract_root)
    archives = sorted(save_root.glob("*.zip"))
    anchors = [read_zip_anchor(path, extract_root, extract=not args.no_extract) for path in archives]
    data = {
        "schema": "earthbound-decomp.mesen-srm-anchor-catalog.v1",
        "status": "catalog_generated_local_srm_bytes_ignored",
        "generated_by": "tools/build_mesen_srm_anchor_catalog.py",
        "save_root": str(save_root),
        "extract_root": manifest_path(extract_root),
        "summary": {
            "archive_count": len(anchors),
            "valid_srm_count": sum(1 for row in anchors if int(row["srm_size"]) == SRM_SIZE),
            "extracted_working_copy_count": sum(1 for row in anchors if row.get("working_srm_path")),
            "source_promotion_allowed": False,
        },
        "anchors": anchors,
    }
    write_json(Path(args.output), data)
    write_text(Path(args.note), render_note(data))
    print(f"Wrote {args.output}")
    print(f"Wrote {args.note}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
