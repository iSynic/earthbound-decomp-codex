#!/usr/bin/env python3
"""Build the active frontier for text/script asset semantics.

This report consumes the generated text-command semantic manifest and emits a
shorter milestone view: which commands are already covered, which remaining
rows are real runtime leaves, which are parser artifacts, and which recovered
localization source commands still lack a runtime mapping.
"""

from __future__ import annotations

import argparse
import json
from collections import Counter
from pathlib import Path
from typing import Any


ROOT = Path(__file__).resolve().parents[1]
DEFAULT_MANIFEST = ROOT / "build" / "text-command-semantics-manifest.json"
DEFAULT_JSON = ROOT / "build" / "text-script-assets-frontier.json"
DEFAULT_MARKDOWN = ROOT / "notes" / "text-script-assets-frontier.md"


def load_manifest(path: Path) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def status_count(rows: list[dict[str, Any]]) -> Counter[str]:
    return Counter(str(row.get("status", "unknown")) for row in rows)


def runtime_label(row: dict[str, Any]) -> str:
    target = row.get("runtime_target")
    if not target:
        return "-"
    value = row.get("runtime_value")
    kind = row.get("runtime_kind")
    if kind == "callback" and value:
        return f"{target} callback={value}"
    return target


def segment_label(row: dict[str, Any]) -> str:
    segments = row.get("top_segments") or []
    if not segments:
        return "-"
    return ", ".join(f"{seg['segment']}:{seg['count']}" for seg in segments[:3])


def build_frontier(manifest: dict[str, Any]) -> dict[str, Any]:
    top_level = manifest["top_level"]
    families = manifest["families"]
    family_rows = [
        {
            **row,
            "family": family["opcode"],
            "family_name": family["name"],
            "dispatcher": family["dispatcher"]["address"],
        }
        for family in families
        for row in family["subcommands"]
    ]
    top_counts = status_count(top_level)
    family_counts = status_count(family_rows)
    runtime_only = [row for row in family_rows if row["status"] == "runtime_only"]
    artifact_candidates = [
        row for row in family_rows if row["status"] == "parsed_artifact_candidate"
    ]
    pseudo_top = [row for row in top_level if row["status"] == "parsed_only"]
    authoring_unmapped = [
        row
        for row in manifest.get("authoring_command_hints", [])
        if row.get("runtime_hint") is None
    ]

    return {
        "generated_by": "tools/build_text_script_asset_frontier.py",
        "source_manifest": str(DEFAULT_MANIFEST.relative_to(ROOT)),
        "summary": {
            "top_level_commands": len(top_level),
            "covered_top_level": top_counts.get("covered", 0),
            "parser_only_top_level": len(pseudo_top),
            "family_subcommands": len(family_rows),
            "covered_family_subcommands": family_counts.get("covered", 0),
            "runtime_only_family_subcommands": len(runtime_only),
            "parsed_artifact_candidates": len(artifact_candidates),
            "authoring_commands_tracked": len(manifest.get("authoring_command_hints", [])),
            "authoring_commands_without_runtime_hint": len(authoring_unmapped),
        },
        "pseudo_top_level": pseudo_top,
        "runtime_only_family_subcommands": runtime_only,
        "parsed_artifact_candidates": artifact_candidates,
        "authoring_unmapped": authoring_unmapped,
    }


def write_markdown(frontier: dict[str, Any], output_path: Path) -> None:
    s = frontier["summary"]
    lines = [
        "# Text Script Assets Frontier",
        "",
        "Generated by `tools/build_text_script_asset_frontier.py` from",
        "`build/text-command-semantics-manifest.json`.",
        "",
        "## Summary",
        "",
        f"- Top-level text commands covered: `{s['covered_top_level']} / {s['top_level_commands']}`",
        f"- Parser-only compressed-bank pseudo-opcodes: `{s['parser_only_top_level']}`",
        f"- Family subcommands covered: `{s['covered_family_subcommands']} / {s['family_subcommands']}`",
        f"- Runtime-only family leaves still worth documenting: `{s['runtime_only_family_subcommands']}`",
        f"- Parsed artifact candidates to keep out of naming work: `{s['parsed_artifact_candidates']}`",
        f"- Manifest-listed recovered localization commands without runtime hints: `{s['authoring_commands_without_runtime_hint']}`",
        "",
        "## Good-Enough Boundary",
        "",
        "For this milestone, text/script assets are good enough when the project can",
        "separate these buckets cleanly:",
        "",
        "- live runtime commands with documented handler semantics",
        "- parser-only compressed-bank pseudo-opcodes",
        "- parser artifacts caused by table, pointer, or compressed-data overlap",
        "- recovered localization authoring commands that are metadata/macros rather",
        "  than direct ROM text VM opcodes",
        "",
        "That boundary lets future extraction or reinsertion tooling avoid treating",
        "every unmatched byte as a new text VM feature.",
        "",
        "For the fuller recovered-source authoring syntax view, see",
        "`notes/localization-authoring-command-frontier.md`.",
        "",
        "## Runtime-Only Family Leaves",
        "",
        "These have live C1 dispatcher cases but no observed hits in the current",
        "decoded text-bank corpus. They are real runtime surface area, but most are",
        "rare menu/system helpers rather than blockers for byte-accurate text-bank",
        "scaffolds.",
        "",
        "| Family | Sub | Name | Runtime | Dispatcher |",
        "| --- | --- | --- | --- | --- |",
    ]
    for row in frontier["runtime_only_family_subcommands"]:
        lines.append(
            f"| `{row['family']}` | `{row['subopcode']}` | `{row['name']}` | "
            f"`{runtime_label(row)}` | `{row['dispatcher']}` |"
        )

    lines.extend(
        [
            "",
            "## Parser Artifact Candidates",
            "",
            "These are parsed hits without live dispatcher cases. Keep them visible,",
            "but do not spend naming effort on them unless their surrounding bytes prove",
            "to be a real command stream.",
            "",
            "| Family | Sub | Name | Parsed hits | Segments | Current read |",
            "| --- | --- | --- | ---: | --- | --- |",
        ]
    )
    for row in frontier["parsed_artifact_candidates"]:
        lines.append(
            f"| `{row['family']}` | `{row['subopcode']}` | `{row['name']}` | "
            f"{row['parsed_hits']} | {segment_label(row)} | {row.get('artifact_reason') or '-'} |"
        )

    lines.extend(
        [
            "",
            "## Parser-Only Top-Level Opcodes",
            "",
            "These are not missing runtime commands. They are compressed-bank selector",
            "pseudo-opcodes produced by the text parser layer.",
            "",
            "| Opcode | Name | Parsed hits | Notes |",
            "| --- | --- | ---: | --- |",
        ]
    )
    for row in frontier["pseudo_top_level"]:
        notes = ", ".join(Path(note).name for note in row.get("notes", [])[:3]) or "-"
        lines.append(
            f"| `{row['opcode']}` | `{row['name']}` | {row['parsed_hits']} | {notes} |"
        )

    lines.extend(
        [
            "",
            "## Recovered Authoring Commands Without Runtime Hints",
            "",
            "These rows come from the text-command semantic manifest's authoring",
            "syntax slice. The fuller recovered-source profile is generated in",
            "`notes/localization-authoring-command-frontier.md`. Treat unmapped",
            "commands as authoring syntax, formatting metadata, or higher-level",
            "macros until a ROM-backed runtime mapping is found.",
            "",
            "| Source command | Count |",
            "| --- | ---: |",
        ]
    )
    for row in frontier["authoring_unmapped"][:40]:
        lines.append(f"| `{row['command']}` | {row['count']} |")

    lines.extend(
        [
            "",
            "## Next Manual Seams",
            "",
            "1. Pick one runtime-only family leaf cluster and add source-backed semantics",
            "   notes without requiring text-bank hits.",
            "2. Use the artifact bucket to improve parser boundary rules around `EDEBUG`,",
            "   `ENEWS`, `EHINT`, and `EBATTLE8`.",
            "3. Split high-count recovered authoring commands into direct VM mappings,",
            "   authoring-only metadata, and macro expansions.",
            "",
        ]
    )
    output_path.write_text("\n".join(lines), encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--manifest", type=Path, default=DEFAULT_MANIFEST)
    parser.add_argument("--json-out", type=Path, default=DEFAULT_JSON)
    parser.add_argument("--markdown-out", type=Path, default=DEFAULT_MARKDOWN)
    args = parser.parse_args()

    manifest = load_manifest(args.manifest)
    frontier = build_frontier(manifest)
    args.json_out.parent.mkdir(parents=True, exist_ok=True)
    args.markdown_out.parent.mkdir(parents=True, exist_ok=True)
    args.json_out.write_text(json.dumps(frontier, indent=2, sort_keys=True), encoding="utf-8")
    write_markdown(frontier, args.markdown_out)
    s = frontier["summary"]
    print(f"wrote {args.json_out}")
    print(f"wrote {args.markdown_out}")
    print(
        "top={covered_top_level}/{top_level_commands} "
        "family={covered_family_subcommands}/{family_subcommands} "
        "runtime_only={runtime_only_family_subcommands} "
        "artifacts={parsed_artifact_candidates}".format(**s)
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
