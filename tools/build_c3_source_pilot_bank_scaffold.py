from __future__ import annotations

import argparse
from pathlib import Path

from rom_tools import find_rom, load_rom
from source_bank_byte_equivalence import (
    ROOT,
    load_json,
    rel,
    render_combined_scaffold,
    resolve_path,
)


BANK = "C3"
DEFAULT_RANGES = ROOT / "build" / "c3-build-candidate-ranges.json"
DEFAULT_EVENT_SCAFFOLD = ROOT / "src" / "c3" / "bank_c3_event_scripts_source_pilot.asar.asm"
DEFAULT_OUTPUT = ROOT / "src" / "c3" / "bank_c3_helpers_asar.asm"


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Build the C3 bank scaffold with the event/script corridor replaced by source pilots."
    )
    parser.add_argument("--ranges", type=Path, default=DEFAULT_RANGES)
    parser.add_argument("--event-scaffold", type=Path, default=DEFAULT_EVENT_SCAFFOLD)
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument("--rom")
    return parser


def main() -> int:
    args = build_parser().parse_args()
    ranges_path = resolve_path(args.ranges)
    event_scaffold = resolve_path(args.event_scaffold)
    output = resolve_path(args.output)
    ranges = load_json(ranges_path)
    modules = [
        module
        for module in ranges.get("ranges", [])
        if module.get("source_path") != "src/c3/script_event_payloads_0000_e450.asm"
    ]
    rom = load_rom(find_rom(args.rom))

    tail = render_combined_scaffold(
        modules,
        rom,
        generated_by="tools/build_c3_source_pilot_bank_scaffold.py",
        purpose="Durable C3 bank scaffold with event/actionscript source-pilot corridor.",
        ranges_path=rel(ranges_path),
        rebuild_command="python tools/build_c3_source_pilot_bank_scaffold.py",
    )
    lines = [
        "; Generated by tools/build_c3_source_pilot_bank_scaffold.py",
        "; Durable C3 bank source-pilot scaffold.",
        ";",
        "; This file uses the event/actionscript source-pilot scaffold for",
        "; C3:0000..E450, then assembles the existing native/data tail modules.",
        "",
        f'incsrc "{rel(event_scaffold)}"',
        "",
        tail,
    ]
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
    print(f"wrote {rel(output)} with event scaffold {rel(event_scaffold)} and {len(modules)} tail modules")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
