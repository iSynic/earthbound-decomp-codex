#!/usr/bin/env python3
"""Emit assembler/debugger labels from the working-name manifest.

The working-name manifest is the reviewable source of truth. This script turns
it into generated artifacts that patch assembly, debugger symbol loaders, and
future source files can consume without hand-copying names from Markdown.
"""

from __future__ import annotations

import argparse
import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable


DEFAULT_MANIFEST = Path("build/working-names-c0-c2.json")
DEFAULT_OUT_DIR = Path("build/labels")
VALID_LABEL_RE = re.compile(r"^[A-Za-z_][A-Za-z0-9_]*$")


@dataclass(frozen=True)
class LabelEntry:
    address: str
    bank: str
    name: str
    confidence: str
    tags: tuple[str, ...]
    evidence: tuple[str, ...]

    @property
    def hex24(self) -> str:
        return "$" + self.address.replace(":", "")

    @property
    def sym_addr(self) -> str:
        return self.address.lower()


def workspace_root() -> Path:
    return Path(__file__).resolve().parent.parent


def load_entries(path: Path, banks: set[str] | None) -> list[LabelEntry]:
    raw = json.loads(path.read_text(encoding="utf-8"))
    entries: list[LabelEntry] = []
    seen_addresses: dict[str, str] = {}
    seen_names: dict[str, str] = {}

    for item in raw["entries"]:
        bank = item["bank"].upper()
        if banks is not None and bank not in banks:
            continue

        name = item["name"]
        address = item["address"].upper()
        if not VALID_LABEL_RE.match(name):
            raise ValueError(f"Label name is not source-safe: {address} = {name!r}")
        if address in seen_addresses and seen_addresses[address] != name:
            raise ValueError(
                f"Address {address} has multiple labels: {seen_addresses[address]!r}, {name!r}"
            )
        if name in seen_names and seen_names[name] != address:
            raise ValueError(
                f"Label {name!r} has multiple addresses: {seen_names[name]}, {address}"
            )
        seen_addresses[address] = name
        seen_names[name] = address

        evidence = tuple(
            f"{ev['note']}:{ev['line']}"
            for ev in item.get("evidence", [])
        )
        entries.append(
            LabelEntry(
                address=address,
                bank=bank,
                name=name,
                confidence=item.get("confidence", "proposed"),
                tags=tuple(item.get("tags", [])),
                evidence=evidence,
            )
        )

    entries.sort(key=lambda entry: (entry.address, entry.name))
    return entries


def banks_suffix(entries: Iterable[LabelEntry]) -> str:
    banks = sorted({entry.bank for entry in entries})
    return "-".join(bank.lower() for bank in banks)


def render_asar(entries: list[LabelEntry], manifest_rel: str, filename: str) -> str:
    lines = [
        "; Generated by tools/emit_source_labels.py",
        f"; Source manifest: {manifest_rel}",
        f"; Include from Asar patches with: incsrc \"build/labels/{filename}\"",
        ";",
    ]
    current_bank = ""
    for entry in entries:
        if entry.bank != current_bank:
            current_bank = entry.bank
            lines.extend(["", f"; Bank {current_bank}"])
        lines.append(f"!{entry.name} = {entry.hex24}")
    return "\n".join(lines) + "\n"


def render_ca65(entries: list[LabelEntry], manifest_rel: str) -> str:
    lines = [
        "; Generated by tools/emit_source_labels.py",
        f"; Source manifest: {manifest_rel}",
        "; Constants use 24-bit SNES CPU addresses.",
        ";",
    ]
    current_bank = ""
    for entry in entries:
        if entry.bank != current_bank:
            current_bank = entry.bank
            lines.extend(["", f"; Bank {current_bank}"])
        lines.append(f"{entry.name} := {entry.hex24}")
    return "\n".join(lines) + "\n"


def render_sym(entries: list[LabelEntry], manifest_rel: str) -> str:
    lines = [
        "#SNES65816",
        "# Generated by tools/emit_source_labels.py",
        f"# Source manifest: {manifest_rel}",
        "",
        "[SYMBOL]",
    ]
    for entry in entries:
        lines.append(f"{entry.sym_addr} {entry.name} ANY 1")
    return "\n".join(lines) + "\n"


def render_tsv(entries: list[LabelEntry]) -> str:
    lines = ["address\tbank\tname\tconfidence\ttags\tevidence"]
    for entry in entries:
        lines.append(
            "\t".join(
                [
                    entry.address,
                    entry.bank,
                    entry.name,
                    entry.confidence,
                    ",".join(entry.tags),
                    ",".join(entry.evidence),
                ]
            )
        )
    return "\n".join(lines) + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--manifest", type=Path, default=DEFAULT_MANIFEST)
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUT_DIR)
    parser.add_argument("--banks", nargs="+", help="Optional bank filter, e.g. C0 C1 C2.")
    args = parser.parse_args()

    root = workspace_root()
    manifest = args.manifest if args.manifest.is_absolute() else root / args.manifest
    out_dir = args.output_dir if args.output_dir.is_absolute() else root / args.output_dir
    banks = {bank.upper() for bank in args.banks} if args.banks else None

    entries = load_entries(manifest, banks)
    if not entries:
        raise ValueError("No labels matched the requested bank set.")

    out_dir.mkdir(parents=True, exist_ok=True)
    suffix = banks_suffix(entries)
    manifest_rel = manifest.relative_to(root).as_posix()

    asar_name = f"working-names-{suffix}.asar.inc"
    outputs = {
        out_dir / asar_name: render_asar(entries, manifest_rel, asar_name),
        out_dir / f"working-names-{suffix}.ca65.inc": render_ca65(entries, manifest_rel),
        out_dir / f"working-names-{suffix}.sym": render_sym(entries, manifest_rel),
        out_dir / f"working-names-{suffix}.tsv": render_tsv(entries),
    }
    for path, content in outputs.items():
        path.write_text(content, encoding="utf-8")

    print(f"Wrote {len(outputs)} label artifacts for {len(entries)} entries to {out_dir.relative_to(root).as_posix()}.")
    for path in outputs:
        print(f"- {path.relative_to(root).as_posix()}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
